package dashboard.readout;

/**
 * Defines the functionality of a class that can be updated via a String.
 * 
 * @author Mark Sabini
 *
 */
public interface StringUpdatable
{
	/**
	 * Updates the class with the specified String.
	 * 
	 * @param str the string with which to update the class
	 */
	public void update(String str);
}
